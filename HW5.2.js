// Функция для подсчета пятниц 13-го с 2000 года до текущей даты
function countFriday13th() {
    var count = 0; // Переменная для хранения количества пятниц 13-го
    var currentDate = new Date(); // Текущая дата
    var startDate = new Date("2000-01-01"); // Начальная дата

    // Цикл выполняется, пока начальная дата меньше текущей даты
    while (startDate < currentDate) {
        // Проверяем, является ли текущая дата пятницей (getDay() возвращает 5 для пятницы) и 13-м числом месяца
        if (startDate.getDay() === 5 && startDate.getDate() === 13) {
            count++; // Если да, увеличиваем счетчик
        }
        startDate.setDate(startDate.getDate() + 1); // Переходим к следующему дню
    }

    return count; // Возвращаем количество пятниц 13-го
}

// Выводим результат подсчета
console.log("Количество пятниц 13-го с 2000 года до сегодняшней даты:", countFriday13th());